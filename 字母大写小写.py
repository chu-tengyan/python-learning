s='hello world, this is an example'
print(s.title())   #每个单词首字母大写
print(s.capitalize())  #字符串中的首字母大写
print(s.upper()) #字符串中的所有字母变为大写
print(s.lower()) #字符串中的所有字母变为小写
print(s.swapcase()) #字符串中的大写字母变为小写，小写字母变为大写

