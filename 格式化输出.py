#整数的输出
print('%o'%100)  #八进制
print('%d'%100)  #十进制
print('%x'%100)  #十六进制

#浮点型的输出
print('%f'%1.11000)
print('%f'%1.1100000)
print('%.3f'%1.11000)
print('%.3f'%1.1100000)
print('%e'%1.11000)
print('%e'%1.1100000)
print('%.3e'%1.11000)
print('%.3e'%1.1100000)
print('%g'%1.11000)
print('%g'%1.1100000)
print('%.3g'%1.11000)
print('%.3g'%1.1100000)


#字符串的输出
str1='hello world'
print('%20s'%str1)
print('%-20s'%str1)
print('%.5s'%str1)
print('%10.5s'%str1)
print('%-10.5s'%str1)